/**
 * Gulp is a 'build tool'. Sass is the only build tool we've seen so far.
 * 
 * A build tool is something designed to 'build' a different copy of our
 * code, usually the one that will actually go live on the internet. This
 * means we have a 'developer version' which can have comments, spacing, etc
 * but can also have a 'production version' which has all comments, spacing, etc
 * removed.
 * 
 * Gulp converts from 'developer version' to 'production version'. Think of sass 
 * as a tool that converts from SCSS to CSS as an analogy.
 */